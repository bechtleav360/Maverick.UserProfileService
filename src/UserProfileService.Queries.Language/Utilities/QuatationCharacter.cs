namespace UserProfileService.Queries.Language.Utilities;

/// <summary>
///     The class contains quotation characters.
/// </summary>
internal class QuotedCharacter
{
    /// <summary>
    ///     Single quotation that is used to recognize a value.
    /// </summary>
    public const char SingleQuotedCharacter = '′';

    /// <summary>
    ///     Another single quotation that is accepted to recognize a value.
    /// </summary>
    public const char SingleQuotedCharacterForStrings = '\'';
}
